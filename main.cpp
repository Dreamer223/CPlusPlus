#include <iostream>
#include <cstdlib>
#include <ctime>

int main() {
    // Задаем массив с фактами
    const char* facts[] = {
        "Кошки спят в среднем 12-16 часов в день.",
        "Пчелы могут распознавать лица.",
        "Медведи питаются перед зимней спячкой, чтобы накопить жир.",
        "Муравьи могут поднимать вещи весом в 50 раз больше своего тела.",
        "Дельфины спят с одним открытым глазом.",
        "Одна пчела производит около 1/12 чайной ложки меда в своей жизни.",
        "Коалы спят около 20 часов в день.",
        "Волки воют, чтобы общаться с другими волками в стае.",
        "Викинги использовали корицу для консервации деревянных зданий.",
        "Слоны могут запоминать до 50 членов своей стаи."
    };

    // Получаем количество фактов
    int numFacts = sizeof(facts) / sizeof(facts[0]);

    // Инициализируем генератор случайных чисел
    std::srand(std::time(0));

    // Генерируем случайный индекс факта и выводим его
    int randomIndex = std::rand() % numFacts;
    std::cout << "Случайный факт: " << facts[randomIndex] << std::endl;

    return 0;
}
